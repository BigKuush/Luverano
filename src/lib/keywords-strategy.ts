// Функция для генерации ключевых слов для товара
export const generateProductKeywords = (product: any) => {
  const baseKeywords = [
    product.title,
    product.brand,
    product.category
  ]

  // Добавляем ключи по материалам
  if (product.material) {
    baseKeywords.push(`${product.material} мебель`)
    baseKeywords.push(`мебель из ${product.material}`)
  }

  // Добавляем ключи по стилю
  if (product.style) {
    baseKeywords.push(`мебель в стиле ${product.style}`)
  }

  // Добавляем локальные ключи
  const localKeywords = [
    `${product.title} москва`,
    `${product.title} спб`,
    `${product.title} краснодар`
  ]

  return [...baseKeywords, ...localKeywords]
}
